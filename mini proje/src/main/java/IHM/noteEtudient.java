private void chargernoteEtudiant(int idEtudiant) {
    DefaultTableModel model = new DefaultTableModel();
    model.setColumnIdentifiers(new String[]{"matiere", "note"});

    String sql = "SELECT m.nom AS matiere, n.valeur AS note FROM note n JOIN matiere m ON n.id = m.id JOIN etudiant e ON n.etudiantcode = e.code WHERE e.id = ?;";

    try (Connection conn = connexion.getConnection();  // Assure-toi dâ€™avoir une classe Connexion avec getConnection()
         PreparedStatement ps = conn.prepareStatement(sql)) {

        ps.setInt(1, idEtudiant);
        ResultSet rs = ps.executeQuery();

        while (rs.next()) {
            String matiere = rs.getString("matiere");
            String note = rs.getString("note");


            model.addRow(new Object[]{matiere, note});
        }

        jTable1.setModel(model);

    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Erreur lors du chargement de note.");
    }
}
