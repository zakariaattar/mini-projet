private void chargerEmploiDuTemps() {
    String sql = "SELECT edt.jour, edt.heureDebut, edt.heurefin, m.nom AS nom_matiere, c.nom AS nom_classe FROM emploi_du_temps edt JOIN matiere m ON edt.matiere = m.id JOIN class c ON edt.classid = c.id WHERE edt.preprofid = ? OR edt.deprofid = ?;";
    try (Connection conn = connexion.getConnection();
         PreparedStatement stmt = conn.prepareStatement(sql)) {

        stmt.setInt(1, idEnseignant);
        stmt.setInt(2, idEnseignant);

        ResultSet rs = stmt.executeQuery();

        // D√©finir mod√®le de tableau
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Jour");
        model.addColumn("Heure D√©but");
        model.addColumn("Heure Fin");
        model.addColumn("Mati√®re");
                model.addColumn("Classe");


        while (rs.next()) {
            model.addRow(new Object[] {
                rs.getString("edt.jour"),
                rs.getString("edt.heureDebut"),
                rs.getString("edt.heurefin"),
                rs.getString("nom_matiere"),
                rs.getString("nom_classe")

            });
        }

        jTable1.setModel(model);

    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Erreur lors du chargement de l'emploi du temps.");
    }}

